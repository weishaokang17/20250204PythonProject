print('北京')
print('欢迎你')
print('------------')
print('北京\n欢迎你') # 遇到\n即换行，可以连续换多行
print('北\n京\n欢\n迎\n你')
print('-----------')
print('北京北京\t欢迎你')
print('hello\toooo') # hello是5个字符 ，一个制表位是8个字符 8-5=3
print('hellooooo')
print('老师说:\'好好学习，天天向上\'')
print('老师说:\"好好学习，天天向上\"')

# 原字符前加r或者R，使转义字符失效
print(r'北\n京\n欢\n迎\n你')
print(R'北\n京\n欢\n迎\n你')

'''
转义字符	描述说明
\n	换行符
\t	水平制表位，用于横向跳到下一个制表位
\”	双引号
\’	单引号
\\	一个反斜杠
'''